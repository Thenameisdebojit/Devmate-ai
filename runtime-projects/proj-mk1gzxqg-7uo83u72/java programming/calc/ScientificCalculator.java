package calc;

public class ScientificCalculator {
    public double square(int a) {
        return a * a;
    }

    public double squareRoot(double a) {
        return Math.sqrt(a);
    }

    public double power(double base, double exponent) {
        return Math.pow(base, exponent);
    }

    public double log(double a) {
        return Math.log(a);
    }
}
